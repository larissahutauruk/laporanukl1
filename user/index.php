            kini budaya modern dari kota telah terbawa ke daerah ini sehingga budaya tradisionalnya mulai memudar, bahkan anak genrasi muda yang berasal dari sana tak lagi mengenal ciri budayanya.
            Hal ini menyebabkan beberapa tempat menyediakan tempat khusus untuk belajar adat istiadat daerah ini untuk mengajar, belajar, sekaligus memperkenalkan.
            <br>
            Tapi tenang saja saat ini sudah lumayan banyak pengrajin tekstil yang mulai mengangkat budaya asli provinsi ini seperti memperbanyak industri pelestarian budaya, salah satunya dengan pembuatan industri tekstil
            pakaian dengan berbahan dasar kain tenun. Sehingga pakaian dengan kain tenun ini akan dibuat sedemikian rupa tanpa menghilangkan ciri khas nya.
