            <li><h3>Suku.</h3></li>
        <p>
            Sumatera Utara merupakan provinsi multietnis dengan Batak, Nias, dan Melayu sebagai penduduk asli wilayah ini.
            Sementara pendatang dari luar pulau Sumatera Utara terbanyak mendatangkan suku Tionghoa dan Jawa.
        </p>
            <li><h3>Keyakinan.</h3></li>
        <p>
            Sumatera Utara mendukung kebebasan beragama, diantaranya:
            <br>
            Islam, Kristen (Protestan dan Katolik), Hindu, Buddha, Konghucu.
            <br>
            Selain itu ada 2 kepercayaan lainnya yang dianut oleh masyarakat Sumatera Utara yaitu: Parmalim, Animisme.
        </p>
            <li><h3>Bahasa.</h3></li>
        <p>
            Pada dasarnya, Sumatera Utara termasuk kedalam negara Indonesia sehingga bahasa yang dipergunakan secara luas adalah bahasa Indonesia.
            <br>
            Suku Melayu Deli mayoritas menuturkan bahasa Indonesia.
            Pesisir timur seperi wilayah Serdang Bedagai, Pangkalan Dodek, Batubara, Asahan, dan Tanjung Balai, memakai Bahasa Melayu.
            Perkotaan, suku Tionghoa lazim menuturkan bahasa Hokkian selain bahasa Indonesia.
        </p>
        <p>
            Pegunungan, suku Batak menuturkan bahasa Batak.
            Bahasa Nias dituturkan di Kepulauan Nias oleh suku Nias.
            Sedangkan orang-orang Pesisir Pantai Barat, menggunakan Bahasa Pesisir.
          </p>
          <li><h3>Logat</h3></li>
          <p>Sering kai cara berbicara orang yang berasal dari daerah ini disalah artikan oleh orang luar.
            Dimana apabila kita mengamati bagaimana oarang asli Suamtera memiliki logat yang kasar atau kebiasaannya yang berteriak.
            Akhirnya tak sedikit orang mengira bahwa ketika warga berbincang sering dikira marah-marah atau emosian.
          </p>
            <br>
