        <br>
            <li><h3>Makanan khas.</h3></li>
        <p>
            Masakan Sumatera Utara sangatlah beragam yang paling terkenal diantaranya ada bika ambon, lemang, mie gomak,arsik, daun ubi,
            dan yang paling tidak boleh terlewatkan dalam acara besar yaitu saksang.
        </p>
        <p>
            Saksang ini berbahan dasar daging anjing atau babi dimana disajikan dalam rasa amat pedas.
            Tak heran apabila Sumatera Utara memiliki ciri khas makanan yang pedas. 
          </p>
            <li><h3>Pakaian adat.</h3></li>
        <p>
            Masyarakat dalam mealakukan upacara adat atau kegiatan penting biasanya mereka akan memakai kebaya dengan berselempangkan atau bersarungkan kain yang disebut dengan ulos.
            Ulos memeiliki nama beragam, ada: Ulos Sadum, Ulos Ragi Hotang, Ulos Suri Suri, dan Ulos Ragi Hidup.
            <br>
            Tak hanya sekedar kain saja, biasanya warga sana akan melengkapinya dengan berbagai perhiasan.
            Ada pula yang menjadikan kain ulos sebagai pengikat dikepala mereka baik itu perempuan atau laki-laki.
        </p>
        </ul>
